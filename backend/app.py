import random
import time
from flask import Flask, jsonify
from flask_cors import CORS
from redis import Redis

app = Flask(__name__)
CORS(app)

try:
    cache = Redis(host="redis-cache", port=6379, decode_responses=True)
except Exception:
    cache = None

QUOTES = [
    "Всяко дълго пътуване започва с една единствена малка стъпка.",
    "Успехът не е окончателен: важен е куражът да продължиш.",
    "Най-добрият начин да предвидиш бъдещето е да го създадеш.",
    "Повярвай, че можеш, и си изминал половината път.",
    "Днешният ден е нова възможност да бъдеш по-добра версия на себе си.",
    "Малките ежедневни подобрения водят до зашеметяващи резултати.",
    "Логиката ще те отведе от точка А до точка Б. Въображението ще те отведе навсякъде.",
    "Не бройте дните, направете така, че дните да се броят.",
    "Единственият начин да вършите добра работа е да обичате това, което правите.",
    "Животът е това, което ни се случва, докато правим планове за бъдещето.",
    "Няма преки пътища до никое място, което си заслужава да посетите.",
    "Когато нещо е достатъчно важно, ти го правиш, дори шансовете да не са на твоя страна.",
    "Противопоставянето ражда истинския характер, а успехът го показва.",
    "Умните хора се учат от всичко и всеки, средните хора – от опита си, глупавите – вече знаят всичко.",
    "Енергията и упоритостта завладяват всичко."
]

@app.route('/quote', methods=['GET'])
def get_quote():
    selected_quote = random.choice(QUOTES)
    
    click_count = 0
    if cache:
        try:
            click_count = cache.incr("total_clicks")
        except Exception:
            click_count = "Няма връзка с Redis"

    return jsonify({
        "quote": selected_quote,
        "clicks": click_count,
        "timestamp": time.strftime("%H:%M:%S")
    })

if __name__ == '__main__':
    app.run(host='0.0.0.0', port=5000)